    private Integer ylId;

    private Integer uid;

    private Integer aid;

    private Date sdate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getYlId() {
        return ylId;
    }

